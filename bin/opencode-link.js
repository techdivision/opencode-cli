// Colors for terminal output
const colors = {
  reset: '\x1b[0m',
  bright: '\x1b[1m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  blue: '\x1b[34m',
  red: '\x1b[31m',
  cyan: '\x1b[36m',
  gray: '\x1b[90m',
  magenta: '\x1b[35m',
};

function log(message, color = 'reset') {
  console.log(`${colors[color]}${message}${colors.reset}`);
}

function logSuccess(message) {
  console.log(`${colors.green}  + ${message}${colors.reset}`);
}

function logOverride(message) {
  console.log(`${colors.yellow}  ~ ${message} (overriding)${colors.reset}`);
}

function logSkipped(message) {
  console.log(`${colors.gray}  - ${message} (skipped)${colors.reset}`);
}

function logError(message) {
  console.log(`${colors.red}  ! ${message}${colors.reset}`);
}

function logInfo(message) {
  console.log(`${colors.cyan}  ℹ ${message}${colors.reset}`);
}

function capitalize(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

/**
 * Formats a plugin label showing location and type.
 * Format: [location/type]
 * 
 * @param {PluginDescriptor} descriptor - The plugin descriptor
 * @returns {string} Formatted label with colors
 */
function formatPluginLabel(descriptor) {
  const locationColors = {
    global: colors.cyan,
    local: colors.magenta,
  };
  const locationColor = locationColors[descriptor.location] || colors.gray;
  return `${locationColor}[${descriptor.location}/${descriptor.type}]${colors.reset}`;
}

// Target directory for linking (set in main() after argument parsing)
let TARGET_DIR = null;
let TARGET_SOURCE = null;
let PLUGINS = null;
let CONTENT_TYPES = null;
let FORCE = false;

/**
 * Gets the OpenCode user config directory path.
 * 
 * @returns {string} Path to ~/.config/opencode
 */
function getUserConfigDir() {
  return path.join(os.homedir(), '.config', 'opencode');
}

/**
 * Checks if the given directory is the OpenCode user config directory.
 * 
 * @param {string} dir - Directory to check
 * @returns {boolean} True if dir is ~/.config/opencode
 */
function isUserConfigDir(dir) {
  return path.resolve(dir) === getUserConfigDir();
}

/**
 * Searches for opencode.json starting from current directory and going up.
 */
function findOpencodeConfig() {
  let currentDir = process.cwd();
  const root = path.parse(currentDir).root;

  while (currentDir !== root) {
    const configPath = path.join(currentDir, 'opencode.json');
    if (fs.existsSync(configPath)) {
      return currentDir;
    }
    currentDir = path.dirname(currentDir);
  }
  return null;
}

/**
 * Resolves the target directory for linking.
 * 
 * Detection order:
 * 1. CLI --target-dir flag
 * 2. OpenCode user config directory (~/.config/opencode)
 * 3. Directory with opencode.json (search upwards)
 * 4. .opencode subdirectory
 * 5. Default: {cwd}/.opencode
 */
function resolveTargetDir(cliTargetDir) {
  if (cliTargetDir) {
    const resolved = path.resolve(process.cwd(), cliTargetDir);
    return {
      targetDir: resolved,
      source: isUserConfigDir(resolved) ? 'user-config' : 'cli',
    };
  }

  const cwd = process.cwd();

  // Check if we're in the OpenCode user config directory
  if (isUserConfigDir(cwd)) {
    return { targetDir: cwd, source: 'user-config' };
  }

  const configDir = findOpencodeConfig();
  if (configDir) {
    return {
      targetDir: configDir,
      source: 'auto',
    };
  }

  // Check if we're already in a .opencode directory
  if (path.basename(cwd) === '.opencode') {
    return { targetDir: cwd, source: 'auto' };
  }

  return {
    targetDir: path.join(cwd, '.opencode'),
    source: 'default',
  };
}

/**
 * Parses CLI arguments.
 */
function parseArgs(args) {
  let targetDir = null;
  let singular = false;
  let force = false;
  const restArgs = [];

  for (const arg of args) {
    if (arg.startsWith('--target-dir=')) {
      targetDir = arg.substring('--target-dir='.length);
    } else if (arg === '--target-dir') {
      logError('--target-dir requires a value (use --target-dir=<path>)');
      process.exit(1);
    } else if (arg === '--singular') {
      singular = true;
    } else if (arg === '--force') {
      force = true;
    } else {
      restArgs.push(arg);
    }
  }

  return {
    commands: restArgs,
    targetDir,
    singular,
    force,
  };
}

/**
 * Ensures target directory structure exists.
 */
function ensureDirectoryStructure() {
  if (!fs.existsSync(TARGET_DIR)) {
    fs.mkdirSync(TARGET_DIR, { recursive: true });
  }

  for (const dir of CONTENT_TYPES) {
    // Normalize to singular for target directory
    const targetDir = normalizeContentType(dir);
    const dirPath = path.join(TARGET_DIR, targetDir);
    if (!fs.existsSync(dirPath)) {
      fs.mkdirSync(dirPath, { recursive: true });
    }
  }

  // Ensure opencode.json exists (skip for user-config directory which uses config.json)
  if (TARGET_SOURCE !== 'user-config') {
    const configPath = path.join(TARGET_DIR, 'opencode.json');
    if (!fs.existsSync(configPath)) {
      const defaultConfig = {
        $schema: 'https://opencode.ai/config.json',
        instructions: ['guideline/*.md'],
      };
      fs.writeFileSync(configPath, JSON.stringify(defaultConfig, null, 2) + '\n');
      logSuccess('opencode.json created');
    }
  }
}

/**
 * Links a single plugin.
 */
async function linkPlugin(pluginName) {
  const descriptor = PLUGINS.get(pluginName);
  if (!descriptor) {
    logError(`Unknown plugin: ${pluginName}`);
    log(`Available plugins: ${Array.from(PLUGINS.keys()).join(', ')}`, 'yellow');
    return false;
  }

  const label = formatPluginLabel(descriptor);
  
  log(`\nLinking ${pluginName} ${label}`, 'cyan');
  log(`  ${descriptor.description}`, 'gray');
  log(`  Package: ${descriptor.packageName}@${descriptor.version}`, 'gray');

  let linkedCount = 0;
  let overrideCount = 0;
  let skippedCount = 0;

  for (const type of descriptor.contentTypes) {
    const sourceDir = getSourceDir(descriptor, type);
    if (!sourceDir) continue;
    
    // Normalize type to singular for target directory (agents -> agent, skills -> skill)
    const targetType = normalizeContentType(type);
    const targetDir = path.join(TARGET_DIR, targetType);

    // Get items to link based on type
    const items = getItemsToLink(sourceDir, type, targetDir, descriptor.pluginName);

    for (const item of items) {
      const { sourcePath, targetPath, displayName } = item;
      const result = createSymlink(sourcePath, targetPath);

      switch (result) {
        case 'created':
          logSuccess(displayName);
          linkedCount++;
          break;
        case 'overridden':
          logOverride(displayName);
          linkedCount++;
          overrideCount++;
          break;
        case 'skipped-same':
          skippedCount++;
          break;
        case 'skipped-real':
          logSkipped(`${displayName} (real file, not overriding)`);
          skippedCount++;
          break;
        case 'error':
          logError(`Failed to link ${displayName}`);
          break;
      }
    }
  }

  if (linkedCount > 0) {
    log(`  Linked: ${linkedCount} items`, 'green');
  }
  if (overrideCount > 0) {
    log(`  Overridden: ${overrideCount} items`, 'yellow');
  }
  if (skippedCount > 0) {
    log(`  Skipped: ${skippedCount} items`, 'gray');
  }

  // Check for @opencode-ai/plugin dependency when linking plugins content type
  if (descriptor.contentTypes.includes('plugins')) {
    checkPluginDependency(descriptor.pluginName);
  }

  // Run postlink hook if configured
  await runPostlinkHook(descriptor);

  return true;
}

/**
 * Checks if @opencode-ai/plugin is listed in .opencode/package.json.
 * Logs a warning if the dependency is missing, since OpenCode plugins
 * require this package to resolve TypeScript types at runtime.
 *
 * @param {string} pluginName - Name of the plugin being linked
 */
function checkPluginDependency(pluginName) {
  const packageJsonPath = path.join(TARGET_DIR, 'package.json');

  try {
    if (!fs.existsSync(packageJsonPath)) {
      logInfo(`Plugin '${pluginName}' uses OpenCode plugins — create .opencode/package.json with @opencode-ai/plugin dependency`);
      return;
    }

    const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, 'utf-8'));
    const deps = packageJson.dependencies || {};
    const devDeps = packageJson.devDependencies || {};

    if (!deps['@opencode-ai/plugin'] && !devDeps['@opencode-ai/plugin']) {
      logInfo(`Plugin '${pluginName}' uses OpenCode plugins — add @opencode-ai/plugin to .opencode/package.json`);
    }
  } catch {
    // Ignore parse/read errors
  }
}

/**
 * Runs the postlink hook for a plugin if one is configured.
 *
 * Discovery order:
 * 1. Declared in plugin.json: { "hooks": { "postlink": "scripts/postlink.js" } }
 * 2. Convention: postlink.js in plugin root directory
 *
 * @param {PluginDescriptor} descriptor - The plugin descriptor
 */
async function runPostlinkHook(descriptor) {
  // 1. Check plugin.json hooks declaration
  let hookPath = null;
  if (descriptor.hooks?.postlink) {
    hookPath = path.join(descriptor.rootDir, descriptor.hooks.postlink);
  }

  // 2. Fallback to convention: postlink.js at plugin root
  if (!hookPath) {
    const conventionPath = path.join(descriptor.rootDir, 'postlink.js');
    if (fs.existsSync(conventionPath)) {
      hookPath = conventionPath;
    }
  }

  if (!hookPath || !fs.existsSync(hookPath)) return;

  log(`  Running postlink hook...`, 'gray');
  try {
    const module = await import(hookPath);
    const hook = module.default;
    if (typeof hook !== 'function') {
      logError(`postlink hook must export a default function (${descriptor.pluginName})`);
      return;
    }
    await hook({
      targetDir: TARGET_DIR,
      pluginName: descriptor.pluginName,
      homeDir: os.homedir(),
      log: {
        success: logSuccess,
        info: logInfo,
        error: logError,
        warning: logSkipped,
      },
    });
  } catch (err) {
    logError(`postlink hook failed (${descriptor.pluginName}): ${err.message}`);
  }
}

/**
 * Scans the target directory to find which plugins are currently linked.
 */
function getLinkedPlugins() {
  const linkedPlugins = new Map();

  for (const type of CONTENT_TYPES) {
    // Normalize to singular for target directory
    const targetType = normalizeContentType(type);
    const dir = path.join(TARGET_DIR, targetType);
    if (!fs.existsSync(dir)) continue;

    const files = fs.readdirSync(dir);
    for (const file of files) {
      const filePath = path.join(dir, file);
      try {
        if (fs.lstatSync(filePath).isSymbolicLink()) {
          const target = fs.readlinkSync(filePath);
          
          // Try to match against known plugins
          for (const [pluginName, descriptor] of PLUGINS.entries()) {
            if (target.includes(descriptor.rootDir)) {
              linkedPlugins.set(pluginName, descriptor);
              break;
            }
          }
        }
      } catch {
        // Ignore errors
      }
    }
  }

  return linkedPlugins;
}

/**
 * Shows current link status.
 */
function showStatus() {
  log('\nOpenCode Plugin Status', 'bright');
  log('='.repeat(50), 'gray');
  log(`Target:  ${TARGET_DIR}`, 'gray');
  log('');

  const linkedPlugins = getLinkedPlugins();
  
  log(`Linked Plugins (${linkedPlugins.size}):`, 'cyan');
  for (const [name, descriptor] of linkedPlugins.entries()) {
    const label = formatPluginLabel(descriptor);
    log(`  ${name} ${label}`, 'green');
  }
  log('');

  for (const type of CONTENT_TYPES) {
    // Normalize to singular for target directory
    const targetType = normalizeContentType(type);
    const dir = path.join(TARGET_DIR, targetType);
    if (!fs.existsSync(dir)) {
      log(`${targetType}/: (not created)`, 'yellow');
      continue;
    }

    const files = fs.readdirSync(dir);
    const symlinks = files.filter((f) => {
      const filePath = path.join(dir, f);
      try {
        return fs.lstatSync(filePath).isSymbolicLink();
      } catch {
        return false;
      }
    });

    log(`${targetType}/: ${symlinks.length} symlinks`, 'cyan');

    for (const file of symlinks) {
      const filePath = path.join(dir, file);
      try {
        const target = fs.readlinkSync(filePath);
        let plugin = 'unknown';
        for (const [name, descriptor] of PLUGINS.entries()) {
          if (target.includes(descriptor.rootDir)) {
            plugin = name;
            break;
          }
        }
        const isDir = fs.statSync(filePath).isDirectory() ? '/' : '';
        log(`  ${file}${isDir} -> [${plugin}]`, 'gray');
      } catch {
        log(`  ${file} -> (broken link)`, 'red');
      }
    }
  }

  log('');
}

/**
 * Removes all symlinks from target directories.
 */
function cleanLinks() {
  log('\nCleaning symlinks...', 'yellow');
  let removedCount = 0;

  // Clean AGENTS.md from project root if it's auto-generated
  const projectRoot = path.dirname(TARGET_DIR);
  const agentsMdPath = path.join(projectRoot, 'AGENTS.md');
  try {
    if (fs.existsSync(agentsMdPath)) {
      if (isAutoGeneratedAgentsMd(agentsMdPath)) {
        fs.unlinkSync(agentsMdPath);
        log(`  - AGENTS.md (auto-generated, project root)`, 'gray');
        removedCount++;
      } else {
        log(`  - AGENTS.md (user file, preserved)`, 'yellow');
      }
    }
  } catch {
    // Ignore errors
  }

  for (const type of CONTENT_TYPES) {
    // Check both original and mapped directory names
    const originalType = type;
    const mappedType = mapContentType(type);
    
    // Create list of directories to check (deduplicated)
    const dirsToCheck = new Set([originalType, mappedType]);
    
    for (const dirName of dirsToCheck) {
      const dir = path.join(TARGET_DIR, dirName);
      if (!fs.existsSync(dir)) continue;

      const files = fs.readdirSync(dir);
      for (const file of files) {
        const filePath = path.join(dir, file);
        try {
          if (fs.lstatSync(filePath).isSymbolicLink()) {
            fs.unlinkSync(filePath);
            log(`  - ${dirName}/${file}`, 'gray');
            removedCount++;
          }
        } catch {
          // Ignore errors
        }
      }
    }
  }

  // Remove empty content type directories (both plural and singular)
  let removedDirs = 0;
  for (const type of CONTENT_TYPES) {
    const pluralDir = path.join(TARGET_DIR, type);
    const singularDir = path.join(TARGET_DIR, mapContentType(type));
    
    for (const dir of [pluralDir, singularDir]) {
      if (fs.existsSync(dir)) {
        try {
          const remaining = fs.readdirSync(dir);
          if (remaining.length === 0) {
            fs.rmdirSync(dir);
            log(`  - Removed empty directory: ${path.basename(dir)}/`, 'gray');
            removedDirs++;
          }
        } catch {
          // Ignore errors (e.g., directory not empty, permission issues)
        }
      }
    }
  }

  // Remove .external-skills/ directory
  const externalSkillsDir = path.join(TARGET_DIR, '.external-skills');
  if (fs.existsSync(externalSkillsDir)) {
    try {
      fs.rmSync(externalSkillsDir, { recursive: true, force: true });
      log(`  - .external-skills/ (removed)`, 'gray');
      removedDirs++;
    } catch {
      logError('Failed to remove .external-skills/');
    }
  }

  log(`\nRemoved ${removedCount} items${removedDirs > 0 ? ` and ${removedDirs} empty directories` : ''}`, 'green');
}

/**
 * Lists available plugins.
 */
function listPlugins() {
  log('\nAvailable Plugins', 'bright');
  log('='.repeat(50), 'gray');

  // Group by category
  const corePlugins = Array.from(PLUGINS.entries()).filter(([, p]) => p.category === 'core');
  const standardPlugins = Array.from(PLUGINS.entries()).filter(([, p]) => p.category === 'standard');
  const optionalPlugins = Array.from(PLUGINS.entries()).filter(([, p]) => p.category === 'optional');

  const showPlugins = (plugins, label, color) => {
    if (plugins.length === 0) return;
    
    log(`\n[${label}]`, color);
    for (const [name, descriptor] of plugins) {
      const pluginLabel = formatPluginLabel(descriptor);
      log(`\n${colors.cyan}${name}${colors.reset} ${pluginLabel}`);
      log(`  ${descriptor.description}`, 'gray');
      log(`  Package: ${descriptor.packageName}@${descriptor.version}`, 'gray');
      log(`  Types: ${descriptor.contentTypes.join(', ')}`, 'gray');
    }
  };

  showPlugins(corePlugins, 'Core Plugins', 'blue');
  showPlugins(standardPlugins, 'Standard Plugins', 'green');
  showPlugins(optionalPlugins, 'Optional Plugins', 'yellow');

  // External Skills section
  const externalSkillsConfig = readExternalSkillsConfig(TARGET_DIR);
  if (externalSkillsConfig.length > 0) {
    const installed = listInstalledExternalSkills(TARGET_DIR);

    log('\nEXTERNAL SKILLS  (configured in opencode-project.json)', 'bright');
    log('─'.repeat(57), 'gray');

    renderExternalSkillsTable(externalSkillsConfig, installed);

    // Show hint for "install all" entries with nothing installed yet
    for (const config of externalSkillsConfig) {
      if (!config.skills || config.skills.length === 0) {
        const fromSource = installed.filter((m) => m.source === config.source);
        if (fromSource.length === 0) {
          console.log(`  ${colors.gray}(all skills from ${config.source} — run external-skills:install to install)${colors.reset}`);
        }
      }
    }
  }

  log('');
}

/**
 * Regenerates schemas and AGENTS.md.
 */
function regenerateArtifacts() {
  log('\nRegenerating artifacts...', 'cyan');
  
  const linkedPlugins = getLinkedPlugins();
  
  // Generate combined schema
  const schemaGenerated = generateCombinedSchema(linkedPlugins, TARGET_DIR);
  if (schemaGenerated) {
    logSuccess('Schema generated: schemas/opencode-project.schema.json');
    ensureProjectConfigSchema(TARGET_DIR);
  } else {
    logInfo('No plugin schemas found, skipping schema generation');
  }
  
  // Generate combined AGENTS.md
  const agentsResult = generateCombinedAgentsMd(linkedPlugins, TARGET_DIR);
  switch (agentsResult) {
    case 'created':
      logSuccess('AGENTS.md created (combined from plugins)');
      break;
    case 'overridden':
      logOverride('AGENTS.md (combined from plugins)');
      break;
    case 'skipped':
      logInfo('AGENTS.md skipped (user file or no plugins with AGENTS.md)');
      break;
    case 'removed':
      logInfo('AGENTS.md removed (no plugins with AGENTS.md)');
      break;
  }
  
  log('\nDone!', 'green');
}

/**
 * Returns a formatted security label string for a skill meta entry.
 *
 * @param {ExternalSkillMeta|undefined} meta
 * @returns {string}
 */
function formatSecurityLabel(meta) {
  if (!meta || !meta.security) return '';
  const risk = getHighestRisk(meta.security);
  if (risk === 'safe' || risk === 'low') return `${colors.green}✓ Safe${colors.reset}  `;
  if (risk === 'medium') return `${colors.yellow}⚠ Medium${colors.reset}  `;
  if (risk === 'high' || risk === 'critical') return `${colors.red}✗ ${capitalize(risk)}${colors.reset}  `;
  return '';
}

/**
 * Builds a deduplicated list of {name, source} entries from config + installed skills.
 *
 * @param {ExternalSkillConfig[]} configured
 * @param {ExternalSkillMeta[]} installed
 * @returns {Array<{name: string, source: string}>}
 */
function buildExternalSkillsRows(configured, installed) {
  const seen = new Set();
  const rows = [];

  for (const config of configured) {
    const names = config.skills && config.skills.length > 0
      ? config.skills
      : installed.filter((m) => m.source === config.source).map((m) => m.name);

    for (const name of names) {
      if (!seen.has(name)) {
        seen.add(name);
        rows.push({ name, source: config.source });
      }
    }
  }

  // Add skills that are installed but no longer in config
  for (const meta of installed) {
    if (!seen.has(meta.name)) {
      seen.add(meta.name);
      rows.push({ name: meta.name, source: meta.source });
    }
  }

  return rows;
}

/**
 * Renders a table of external skills to stdout.
 *
 * @param {ExternalSkillConfig[]} configured
 * @param {ExternalSkillMeta[]} installed
 */
function renderExternalSkillsTable(configured, installed) {
  const installedNames = new Set(installed.map((m) => m.name));
  const rows = buildExternalSkillsRows(configured, installed);

  const COL_NAME = 32;
  const COL_SOURCE = 28;

  for (const row of rows) {
    const isInstalled = installedNames.has(row.name);
    const meta = installed.find((m) => m.name === row.name);
    const name = row.name.substring(0, COL_NAME - 1).padEnd(COL_NAME);
    const source = (row.source || '').substring(0, COL_SOURCE - 1).padEnd(COL_SOURCE);
    const securityLabel = formatSecurityLabel(meta);
    const statusLabel = isInstalled
      ? `${colors.green}installed${colors.reset}`
      : `${colors.gray}not installed${colors.reset}`;

    console.log(`  ${colors.cyan}${name}${colors.reset} ${colors.gray}${source}${colors.reset} ${securityLabel}${statusLabel}`);
  }
}

/**
 * Searches skills.sh and displays results in a formatted table.
 *
 * @param {string} query
 */
async function runExternalSkillsFind(query) {
  if (!query || !query.trim()) {
    logError('Usage: opencode-link external-skills:find <query>');
    process.exit(1);
  }

  log(`\nExternal Skills from skills.sh — "${query.trim()}"`, 'bright');
  log('─'.repeat(70), 'gray');

  const skills = await searchSkills(query.trim());

  if (skills.length === 0) {
    logInfo('No skills found.');
    return;
  }

  // Header
  const COL_SKILL = 28;
  const COL_REPO = 26;
  const COL_INST = 10;
  const header = `  ${'SKILL'.padEnd(COL_SKILL)} ${'REPOSITORY'.padEnd(COL_REPO)} INSTALLS`;
  log(header, 'gray');

  for (const skill of skills) {
    const name = (skill.name || skill.slug || '').substring(0, COL_SKILL - 1).padEnd(COL_SKILL);
    const source = (skill.source || '').substring(0, COL_REPO - 1).padEnd(COL_REPO);
    const installs = formatInstalls(skill.installs || 0).padEnd(COL_INST);
    console.log(`  ${colors.cyan}${name}${colors.reset} ${colors.gray}${source}${colors.reset} ${installs}`);
  }

  // Show install hint using the :add command
  const firstSource = skills[0]?.source || '<owner/repo>';
  const firstName = skills[0]?.name || skills[0]?.slug || '<skill-name>';

  log(`\nFor the complete list see https://skills.sh/?q=${encodeURIComponent(query.trim())}`, 'gray');
  log('\nTo install:', 'gray');
  console.log(`  ${colors.cyan}opencode-link external-skills:add ${firstSource} ${firstName}${colors.reset}`);
}

/**
 * Displays a list of installed external skills.
 *
 * @param {string} targetDir
 */
function showExternalSkillsList(targetDir) {
  const installed = listInstalledExternalSkills(targetDir);
  const configured = readExternalSkillsConfig(targetDir);

  log('\nExternal Skills', 'bright');
  log('─'.repeat(70), 'gray');

  if (installed.length === 0 && configured.length === 0) {
    logInfo('No external skills configured. Add "externalSkills" to .opencode/opencode-project.json.');
    return;
  }

  renderExternalSkillsTable(configured, installed);

  log('');
}

/**
 * Main entry point.
 */
async function main() {
  const args = process.argv.slice(2);
  const { commands, targetDir: cliTargetDir, singular, force } = parseArgs(args);

  // Enable singular directory mode if --singular flag is passed
  if (singular) {
    setUseSingularDirs(true);
  }

  FORCE = force;

  // Resolve target directory
  const { targetDir, source } = resolveTargetDir(cliTargetDir);
  TARGET_DIR = targetDir;
  TARGET_SOURCE = source;

  // Discover plugins
  PLUGINS = discoverPlugins(TARGET_DIR);
  CONTENT_TYPES = getContentTypes(PLUGINS);

  log('\nOpenCode Plugin Linker', 'bright');
  log('='.repeat(50), 'gray');

  // Show target directory info
  if (source === 'user-config') {
    log(`Target: ${TARGET_DIR} (user config directory)`, 'cyan');
  } else if (source === 'cli') {
    log(`Target: ${TARGET_DIR} (from --target-dir)`, 'cyan');
  } else if (source === 'auto') {
    log(`Target: ${TARGET_DIR} (auto-detected opencode.json)`, 'cyan');
  } else {
    log(`Target: ${TARGET_DIR} (default)`, 'gray');
  }

  log(`Discovered: ${PLUGINS.size} plugins`, 'gray');

  // No command: link all standard plugins
  if (commands.length === 0) {
    ensureDirectoryStructure();
    const standardPlugins = Array.from(PLUGINS.entries())
      .filter(([, p]) => p.category === 'standard')
      .map(([name]) => name);
    
    for (const pluginName of standardPlugins) {
      await linkPlugin(pluginName);
    }
    
    // Generate artifacts
    log('');
    regenerateArtifacts();

    // Install and link external skills if configured
    const externalSkillsConfig = readExternalSkillsConfig(TARGET_DIR);
    if (externalSkillsConfig.length > 0) {
      await installExternalSkills(TARGET_DIR, { force: FORCE });
      linkExternalSkills(TARGET_DIR);
    }
    
    log(`\nLinked ${standardPlugins.length} standard plugins. Use 'npx opencode-link all' for all plugins.`, 'gray');
    return;
  }

  // external-skills:find consumes all remaining args as the search query
  if (commands[0] === 'external-skills:find') {
    await runExternalSkillsFind(commands.slice(1).join(' '));
    return;
  }

  // external-skills:add <owner/repo> <skill-name>
  if (commands[0] === 'external-skills:add') {
    const ownerRepo = commands[1];
    const skillName = commands[2];
    if (!ownerRepo || !skillName) {
      logError('Usage: opencode-link external-skills:add <owner/repo> <skill-name>');
      logError('Example: opencode-link external-skills:add vercel-labs/agent-skills vercel-react-best-practices');
      process.exit(1);
    }
    await addExternalSkill(TARGET_DIR, ownerRepo, skillName, { force: FORCE });
    return;
  }

  // Process commands
  for (const command of commands) {
    switch (command) {
      case 'status':
        showStatus();
        break;

      case 'clean':
        cleanLinks();
        break;

      case 'list':
        listPlugins();
        break;

      case 'schema':
        regenerateArtifacts();
        break;

      case 'all':
        ensureDirectoryStructure();
        for (const pluginName of PLUGINS.keys()) {
          await linkPlugin(pluginName);
        }
        log('');
        regenerateArtifacts();
        log('');
        showStatus();
        break;

      case 'external-skills:install':
        await installExternalSkills(TARGET_DIR, { force: FORCE });
        linkExternalSkills(TARGET_DIR);
        break;

      case 'external-skills:update':
        await updateExternalSkills(TARGET_DIR);
        linkExternalSkills(TARGET_DIR);
        break;

      case 'external-skills:list':
        showExternalSkillsList(TARGET_DIR);
        break;

      default:
        ensureDirectoryStructure();
        if (await linkPlugin(command)) {
          log('');
          regenerateArtifacts();
          log('\nDone!', 'green');
        } else {
          process.exit(1);
        }
        break;
    }
  }
}

main();
